package assignment;

public class DoubleToRest {
	public static void main(String[] args) {
		 // DOUBLE
        double d = 99.99;
        System.out.println("\nDouble value: " + d);
        
	    byte b = (byte) d;    
	    short s = (short)d;
	    int i = (int)d;
	    long l=(long)d;
	    float f=(float)d;
	    char c = (char) d;
	    
	    System.out.println("\nDouble to byte: " + b);
	    System.out.println("\nDouble to short: " + s);
	    System.out.println("\nDouble to int: " + i);
	    System.out.println("\nDouble to long: " + l);
	    System.out.println("\nDouble to float: " + f);
	    System.out.println("\nDouble to char: " + c);
		}

}
