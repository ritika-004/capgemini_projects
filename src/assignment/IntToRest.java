package assignment;

public class IntToRest {
	public static void main(String[] args) {
		
	    int i = 100;
	    System.out.println("\nInt value: " + i);

	    byte b = (byte) i;    
	    short s = (short)i;
	    long l = i;
	    float f = i;
	    double d = i;
	    char c = (char) i;
	    
	    System.out.println("\nInt to byte: " + b);
	    System.out.println("\nInt to short: " + s);
	    System.out.println("\nInt to long: " + l);
	    System.out.println("\nInt to float: " + f);
	    System.out.println("\nInt to double: " + d);
	    System.out.println("\nInt to char: " + c);
		}

}
