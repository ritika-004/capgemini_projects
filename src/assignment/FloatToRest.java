package assignment;

public class FloatToRest {
	public static void main(String[] args) {
		// FLOAT
        float f = 10.5f;
        System.out.println("\nFloat value: " + f);

	    byte b = (byte) f;    
	    short s = (short)f;
	    int i = (int)f;
	    long l=(long)f;
	    double d = f;
	    char c = (char) f;
	    
	    System.out.println("\nFloat to byte: " + b);
	    System.out.println("\nFloat to short: " + s);
	    System.out.println("\nFloat to int: " + i);
	    System.out.println("\nFloatto long: " + l);
	    System.out.println("\nFloat to double: " + d);
	    System.out.println("\nFloat to char: " + c);
		}


}
